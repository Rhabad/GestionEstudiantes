    private void btnInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInscribirActionPerformed
        Conexion con = new Conexion();
        
        String rut = "";
        String nombres = "";
        String apellidoP = "";
        String apellidoM = "";
        String carrera = "...";
        String anio = "", mes = "", dia = "";
        
        String fechaNacimiento = "";
        int idCarrera = 0;
        
        //si preciona el boton...
        
        rut = txtRut.getText();
        nombres = txtNombres.getText();
        apellidoP = txtApellidoP.getText();
        apellidoM =  txtApellidoM.getText();
        carrera = comboCarrera.getSelectedItem().toString();
        anio = txtAnio.getText();
        mes = txtMes.getText();
        dia = txtDia.getText();
        
        //combinamos dia mes anio
        fechaNacimiento = anio+"-"+mes+"-"+dia;
        
        
        
        //en base a lo que quizo inscribirse, se asgina un numero
        if (carrera.equals("Psicologia")) {
            idCarrera = 1;
        }
        //informatica
        if (carrera.equals("Informatica")) {
            idCarrera = 2;
        }
        //medicina
        if (carrera.equals("Medicina")) {
            idCarrera = 3;
        }
        
        //si minimo un campo esta vacio... debera completarlo
        if (rut.equals("") || nombres.equals("") || apellidoP.equals("") || apellidoM.equals("")
                || carrera.equals("...") || anio.equals("") || mes.equals("") || dia.equals("")) {
            JOptionPane.showMessageDialog(null, "Completa todos los campos");
            
        } else { //si tiene todos los campo rellenos, los muestra en pantalla
            
            //convertimos el rut srting en int
            int numeroRut = Integer.parseInt(rut);
            
            //ingresar alumno nuevo a base de datos
            con.agregarAlumnos(numeroRut, nombres, apellidoP, apellidoM, fechaNacimiento, idCarrera);
            
            JOptionPane.showMessageDialog(null, "======= ALUMNO AGREGADO ======="
                    + "\nRut: "+rut+""
                    + "\nNombres: "+nombres+""
                    + "\nApellidos: "+apellidoP+" "+apellidoM+""
                    + "\nCarrera: "+carrera+ ""
                    + "\nFecha Nacimiento: "+anio+"-"+mes+"-"+dia);
        }
        
        
        
        
        
    }//GEN-LAST:event_btnInscribirActionPerformed

